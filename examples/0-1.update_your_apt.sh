# (Step1) Update your apt environment (update the database)

$ sudo apt update

# ----------------------------------------------------------

# (Step2) Upgrade your apt environment (update the installed packages)

$ sudo apt upgrade

# ----------------------------------------------------------

# (Step3) Install important commands

$ sudo apt install cu gawk nkf
